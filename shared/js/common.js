//--------------------------------------------------------------------------------//
// ページトップボタン
//--------------------------------------------------------------------------------//

$(function () {
    var topBtn = $('#pagetop');
    //スクロールしてトップ
    topBtn.click(function () {
        $('body,html').animate({
            scrollTop: 0
        }, 1000);
        return false;
    });


    //--------------------------------------------------------------------------------//
    // ページ内リンク
    //--------------------------------------------------------------------------------//
    var scroll = new SmoothScroll('a[href*="#"]', {
        header: '#header',
        speed: 1000,
        speedAsDuration: true
    });
});
